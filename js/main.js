refs.hamburger.addEventListener('click', () => {
  setMenuOpen(!refs.navLinks.classList.contains('open'));
});

refs.navOverlay.addEventListener('click', () => {
  setMenuOpen(false);
});

refs.navAnchors.forEach((anchor) => {
  anchor.addEventListener('click', () => {
    setMenuOpen(false);
  });
});

refs.filterButtons.forEach((button) => {
  button.addEventListener('click', () => {
    setProjectFilter(button.dataset.f);
  });
});

refs.form.addEventListener('submit', handleFormSubmit);

document.addEventListener('keydown', (event) => {
  if (event.key === 'Escape') {
    setMenuOpen(false);
  }
});

window.addEventListener('scroll', () => {
  if (!scrollTicking) {
    window.requestAnimationFrame(updateScrollState);
    scrollTicking = true;
  }
}, { passive: true });

window.addEventListener('resize', () => {
  if (window.innerWidth > 768) {
    setMenuOpen(false);
  }

  resizeCanvas();
  updateScrollState();
  startMatrix();
});

document.addEventListener('visibilitychange', () => {
  if (document.hidden) {
    stopMatrix();
  } else {
    startMatrix();
  }
});

addMediaChangeListener(reducedMotionQuery, () => {
  connectRevealObserver();
  restartTyping();

  if (reducedMotionQuery.matches) {
    stopMatrix();
    clearCanvas();
  } else {
    startMatrix();
  }
});

addMediaChangeListener(preferredThemeQuery, (event) => {
  if (storage.get('theme')) {
    return;
  }

  theme = event.matches ? 'dark' : 'light';
  applyTheme({ persist: false });
});

applyTheme({ persist: false });
applyLanguage({ persist: false });
connectRevealObserver();
setProjectFilter('all');
updateScrollState();
setMenuOpen(false);
startMatrix();
