function t(key, params = {}) {
  const value = T[lang][key] ?? T.en[key] ?? '';
  return Object.entries(params).reduce(
    (result, [paramKey, paramValue]) => result.replaceAll(`{${paramKey}}`, String(paramValue)),
    value
  );
}

function setMetaContent(element, value) {
  if (element) {
    element.setAttribute('content', value);
  }
}

function translatePage() {
  const year = new Date().getFullYear();

  document.title = t('meta.title');
  html.lang = lang;

  setMetaContent(meta.description, t('meta.description'));
  setMetaContent(meta.ogTitle, t('meta.title'));
  setMetaContent(meta.ogDescription, t('meta.description'));
  setMetaContent(meta.ogLocale, t('meta.locale'));
  setMetaContent(meta.twitterTitle, t('meta.title'));
  setMetaContent(meta.twitterDescription, t('meta.description'));

  document.querySelectorAll('[data-i18n]').forEach((element) => {
    element.textContent = t(element.dataset.i18n, { year });
  });

  document.querySelectorAll('[data-i18n-placeholder]').forEach((element) => {
    element.setAttribute('placeholder', t(element.dataset.i18nPlaceholder));
  });

  refs.langBtn.textContent = lang === 'en' ? 'RU' : 'EN';
  refs.langBtn.setAttribute('aria-label', lang === 'en' ? t('a11y.switchToRu') : t('a11y.switchToEn'));

  renderThemeButton();
  renderMenuButton();
  renderFormButton();
  setFormStatus(formStatusTone, formStatusKey);
  restartTyping();
}

